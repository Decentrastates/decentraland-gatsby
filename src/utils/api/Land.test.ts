import Land from './Land';


describe('utils/api/Land', () => {
  test('encodeParcelId', () => {
    expect(Land.get().encodeParcelId([0,0])).toBe("0")
    expect(Land.get().encodeParcelId([1,0])).toBe("340282366920938463463374607431768211456")
    expect(Land.get().encodeParcelId([1,1])).toBe("340282366920938463463374607431768211457")
    expect(Land.get().encodeParcelId([0,1])).toBe("1")

    expect(Land.get().encodeParcelId([0,-1])).toBe("340282366920938463463374607431768211455")
    expect(Land.get().encodeParcelId([-1,0])).toBe("115792089237316195423570985008687907852929702298719625575994209400481361428480")
    expect(Land.get().encodeParcelId([-1,-1])).toBe("115792089237316195423570985008687907853269984665640564039457584007913129639935")

    expect(Land.get().encodeParcelId([24,24])).toBe('8166776806102523123120990578362437074968')
    expect(Land.get().encodeParcelId([24,-24])).toBe('8507059173023461586584365185794205286376')
    expect(Land.get().encodeParcelId([-24,-24])).toBe('115792089237316195423570985008687907845443490226458979379799968036982460776424')
    expect(Land.get().encodeParcelId([-24,24])).toBe('115792089237316195423570985008687907845103207859538040916336593429550692565016')

    expect(Land.get().encodeParcelId([120,120])).toBe('40833884030512615615604952891812185374840')
    expect(Land.get().encodeParcelId([120,-120])).toBe('41174166397433554079068327499243953586056')
    expect(Land.get().encodeParcelId([-120,-120])).toBe('115792089237316195423570985008687907812776383002048886887316005723532712476552')
    expect(Land.get().encodeParcelId([-120,120])).toBe('115792089237316195423570985008687907812436100635127948423852631116100944265336')
  })
})