import { toPrefixedNumber } from './utils'

test('toPrefixedNumber', () => {
  expect(toPrefixedNumber(0)).toBe('0')
  expect(toPrefixedNumber(0, 5)).toBe('00000')
  expect(toPrefixedNumber(123)).toBe('123')
  expect(toPrefixedNumber(123, 2)).toBe('123')
  expect(toPrefixedNumber(123, 5)).toBe('00123')
  expect(toPrefixedNumber(0.123)).toBe('0.123')
  expect(toPrefixedNumber(0.123, 2)).toBe('00.123')
  expect(toPrefixedNumber(0.123, 5)).toBe('00000.123')
  expect(toPrefixedNumber(123.123)).toBe('123.123')
  expect(toPrefixedNumber(123.123, 2)).toBe('123.123')
  expect(toPrefixedNumber(123.123, 5)).toBe('00123.123')
  expect(toPrefixedNumber(-0)).toBe('0')
  expect(toPrefixedNumber(-0, 5)).toBe('00000')
  expect(toPrefixedNumber(-123)).toBe('-123')
  expect(toPrefixedNumber(-123, 2)).toBe('-123')
  expect(toPrefixedNumber(-123, 5)).toBe('-00123')
  expect(toPrefixedNumber(-0.123)).toBe('-0.123')
  expect(toPrefixedNumber(-0.123, 2)).toBe('-00.123')
  expect(toPrefixedNumber(-0.123, 5)).toBe('-00000.123')
  expect(toPrefixedNumber(-123.123)).toBe('-123.123')
  expect(toPrefixedNumber(-123.123, 2)).toBe('-123.123')
  expect(toPrefixedNumber(-123.123, 5)).toBe('-00123.123')
})